package Lesson5.Figures;

public class Figure {
    private String colorFilling;
    private String colorBorder;

    public String getColorFilling() {
        return colorFilling;
    }

    public void setColorFilling(String colorFilling) {
        this.colorFilling = colorFilling;
    }

    public String getColorBorder() {
        return colorBorder;
    }

    public void setColorBorder(String colorBorder) {
        this.colorBorder = colorBorder;
    }
}
